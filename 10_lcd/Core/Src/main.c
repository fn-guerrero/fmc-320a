/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{
	/* USER CODE BEGIN 1 */
	char msg_0[20];
	char msg_1[20];
	uint8_t test = 1;

	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_SPI1_Init();
	MX_RTC_Init();
	MX_TIM16_Init();
	/* USER CODE BEGIN 2 */

	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */

	/*
	 * AL iniciar el modulo se encienden todos los segmentos.
	 * Mantengo 1 segundo esta situacion antes de apagarlos.
	 */
	lcd_module_init();
	HAL_Delay(1000);
	pcf8553_write_all(0X00);

	HAL_RTCEx_SetWakeUpTimer_IT(&hrtc, 4096, RTC_WAKEUPCLOCK_RTCCLK_DIV16); //
	HAL_TIM_Base_Start(&htim16);

	while (1)
	{

		switch (test)
		{
			case 1:

				/*
				 * Incremnento dos condadores, muestro cada uno en la primera
				 * y segunda linea del LCD
				 */
				lcd_module_utoa_right(msg_0, time_start, LCD_ROW_0);
				lcd_module_utoa_right(msg_1, time_end, LCD_ROW_1);
				lcd_module_puts(msg_0, LCD_ROW_0);
				lcd_module_puts(msg_1, LCD_ROW_1);
				lcd_module_refresh();
				time_start++;
				time_end++;
				HAL_Delay(1000);
			break;
			case 2:
				/*
				 * Se mide la costo en micro-segundos para imprimir ambas lineas del
				 * LCD.
				 * En la linea 1 se imprime el valor que tiene el timer 6 al inicio
				 * En la linea 2 se imprimen los micro-segundos consumidos.
				 */

				time_start = __HAL_TIM_GET_COUNTER(&htim16);

				lcd_module_utoa_right(msg_0, time_start, LCD_ROW_0);
				lcd_module_utoa_right(msg_1, time_delta, LCD_ROW_1);

				lcd_module_puts(msg_0, LCD_ROW_0);
				lcd_module_puts(msg_1, LCD_ROW_1);

				lcd_module_refresh();

				time_end = __HAL_TIM_GET_COUNTER(&htim16);
				time_delta = time_end - time_start;

				HAL_SuspendTick();
				HAL_PWREx_EnterSTOP2Mode(PWR_STOPENTRY_WFI); // stop1 mode
				SystemClock_Config(); //al salir del modo de bajo consumo hay que volver a configurar el clock
				HAL_ResumeTick();

			break;
			default:
			break;
		}

		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */
	}
	/* USER CODE END 3 */
}

